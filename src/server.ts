let server: Server;
const host = '0.0.0.0';

function startServerWithRetry(startPort: number, attemptsLeft: number, onStarted: (port: number) => void) {
  const attemptPort = startPort;
  const s = app.listen(attemptPort, host, () => {
    server = s;
    onStarted(attemptPort);
  });
  s.once('error', (err: any) => {
    if (err && err.code === 'EADDRINUSE' && attemptsLeft > 0) {
      pino({ level: env.LOG_LEVEL }).warn({ port: attemptPort }, 'Port in use, trying next');
      try { s.close(); } catch {}
      startServerWithRetry(attemptPort + 1, attemptsLeft - 1, onStarted);
    } else {
      logger.error({ err, port: attemptPort }, 'Failed to start server');
      process.exit(1);
    }
  });
}

validateEnvForStartup();
startServerWithRetry(env.PORT, 10, (boundPort) => {
  const baseUrl = `http://localhost:${boundPort}`;
  logger.info({ port: boundPort, docsUrl: `${baseUrl}/docs`, healthUrl: `${baseUrl}/api/health` }, 'Server listening');
});

let isShuttingDown = false;
const shutdown = async (signal: NodeJS.Signals) => {
  if (isShuttingDown) return;
  isShuttingDown = true;
  logger.info({ signal }, "Shutting down");
  try { const mod = await import("./db/pool"); await mod.closePool(); } catch (e) { logger.error(e); }
  server.close((err?: Error) => {
    if (err) {
      logger.error({ err }, 'Error closing HTTP server');
      process.exit(1);
    }
    process.exit(0);
  });
  setTimeout(() => {
    logger.error('Forced shutdown after timeout');
    process.exit(1);
  }, 10000).unref();
};
process.on("SIGTERM", () => void shutdown("SIGTERM"));
process.on("SIGINT", () => void shutdown("SIGINT"));

